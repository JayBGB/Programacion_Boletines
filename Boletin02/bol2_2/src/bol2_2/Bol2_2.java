//Calcular el área de un cuadrado de 3 metros de lado.
package bol2_2;


public class Bol2_2 {

    
    public static void main(String[] args) {
        
        float lado = 3;
        float area = (float) Math.pow(lado, 2);
        
        System.out.println("Un cuadrado cuyos lados miden "+lado+" metros tiene un área de "+area+" metros.");
        
    }
    
}
